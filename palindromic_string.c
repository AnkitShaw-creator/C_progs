# include<stdio.h>

int main(int argc, char const *argv[])
{
    int T;
    scanf("%d",&T);
    while (T--)
    {
        char A[1000],B[1000];
        gets(A);
        gets(B);
        for(int i=0;i<1000;i++){
            if(A[i]==NULL||B[i]==NULL)
                break;
            
        }
    }
    
    return 0;
}
